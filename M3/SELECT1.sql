SELECT
    FIRST_NAME,
    LAST_NAME
FROM
    CONTACTS
ORDER BY
    1,
    2 DESC;

SELECT
    COUNTRY_ID,
    CITY,
    STATE
FROM
    LOCATIONS
ORDER BY
    STATE ASC NULLS FIRST;

SELECT
    CUSTOMER_ID,
    NAME
FROM
    CUSTOMERS
ORDER BY
    UPPER(NAME);

SELECT
    *
FROM
    ORDERS
ORDER BY
    ORDER_DATE DESC;

SELECT
    DISTINCT FIRST_NAME
FROM
    CONTACTS
ORDER BY
    1 ASC;

SELECT
    DISTINCT ITEM_ID,
    PRODUCT_ID,
    UNIT_PRICE,
    QUANTITY
FROM
    ORDER_ITEMS
WHERE
    ITEM_ID='1';

ORDER BY QUANTITY ASC;

SELECT
    DISTINCT ITEM_ID,
    QUANTITY,
    UNIT_PRICE
FROM
    ORDER_ITEMS
WHERE
    ITEM_ID='1'
ORDER BY
    UNIT_PRICE;